    {
        // Implement filter and offset
        $limit = (int)$request->query("limit", 10);
        $offset = (int)$request->query("offset", 0);
        $data = Transaksi::offset($offset)->limit($limit)->get();
        return response()->json([
            "status_code" => 200,
            "data" => [
                "list" => $data,
                "total_items" => Transaksi::count(),
                "offset" => $offset,
                "limit" => $limit
            ]
        ], 200);
    }

